package JavaClasses;

public class graphC {
    int XVal, YVal;

    public graphC(){

    }

    public graphC(int XVal, int YVal) {
        this.XVal = XVal;
        this.YVal = YVal;
    }

    public int getXVal() {
        return XVal;
    }

    public void setXVal(int XVal) {
        this.XVal = XVal;
    }

    public int getYVal() {
        return YVal;
    }

    public void setYVal(int YVal) {
        this.YVal = YVal;
    }
}
